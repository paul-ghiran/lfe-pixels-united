function numarLaPatrat(numar) {
  return numar * numar;
}

const squareNumber = (number) => {
  return number * number;
};

const numberPowerOfTwo = (number) => number * number; // also called 'implicit return'

console.log(numarLaPatrat("3"));
console.log(squareNumber("3"));
console.log(numberPowerOfTwo("3"));

